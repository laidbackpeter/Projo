			<p>
				It's something you probably don't ever want to contemplate but it's equally something that you can all too easily get caught up in when in a bank, store or other place that handles money and seems like an easy picking for a robber.

				While staff are often trained in how to react, as a customer or passer by, you may not know what to do and as such, your reactions might jeopardize your safety unless you're certain about how to react. In this article, you'll learn about how to keep calm and level-headed during a robbery, so that you can survive it as unscathed as possible.
			</p>

			<h1>Steps</h1>
			<br />

			<p>
				<img src="images/RobberyStep1.jpg" width="100%">
				<h2>1</h2>
				Be aware of what is going on around you. Whenever you're in a place that could potentially be held up, such
				as a bank or a small store, stay alert. If you notice suspicious activity, don't go near it but leave and report it
				as soon as it is safe to do so. Trying to keep out of any hairy situation in the first place is the best way to remain calm.

			</p>

			<p>
				<img src="images/RobberyStep2.jpg" width="100%">
				<h2>2</h2>
				Cooperate. Before anything else, signal your intention to the robbers that you intend to cooperate. This isn't the time for heroics and defiance. Make it obvious that you are acquiescing by responding to their requests promptly and not back chatting to any of their demands. Cooperating will help you to stay calm because your chances of surviving the incident are greatly increased by doing nothing to set off the volatility of the robbers.
				<ul>
					<li>
						Remind yourself that the incident will be over faster if you comply with their demands.
					</li>
					<li>
						Read How to survive a bank robbery for more details on what to do during the event of a robbery to increase your chances of walking away unharmed.
					</li>
				</ul>

			</p>

			<p>
				<img src="images/RobberyStep3.jpg" width="100%">
				<h2>3</h2>
				Focus on staying calm. If you feel panic beginning to rise, it's important to try and slow down your thoughts and remain focused on pulling through. Signs of panic include a racing pulse, dizziness, sweating and cramps in your hands and feet; it will often progress to hyperventilation (difficulty in breathing), which can last for several hours.[1] As you can tell, these symptoms risk dulling your senses and getting you through the situation with your wits about you because your body starts overriding your mind. If you do feel panic rising, try the following:
				<ul>
					<li>
						Aim to decrease the speed at which you're breathing. Focus on taking deeper breaths, more slowly.
					</li>
					<li>
						Relax all of your muscles. Do this by tensing them up deliberately for a few seconds and then letting them relax. You can do this unnoticed whether standing, sitting or lying down.
					</li>
					<li>
						Return to focusing on your breathing.
					</li>
				</ul>

			</p>

			<p>
				<img src="images/RobberyStep4.jpg" width="100%">
				<h2>4</h2>
				Listen as well as you can. Apart from gathering potentially useful details, listening carefully can help to keep you calm as you'll be more aware of what the robbers intend to do as they talk. Also try to watch the robbers' movements if doing so doesn't endanger you, as body language can help you gauge what they intend to do next.

			</p>

			<p>
				<img src="images/RobberyStep5.jpg" width="100%">
				<h2>5</h2>
				Expect swearing, shouting and other unnerving noise and actions or reactions. If you're prepared for volatile, scared and crazy behavior from the robbers, at least you're on guard for the worst and can try to focus on protecting yourself, seeking any valid exit possibilities or finding ways to keep yourself out of harm's way (and unnoticed).
				<ul>
					<li>
						Avoid any sudden moves; most robbers are as nervous as you are and anything can set them off. If you do need to move, reach or change your position, tell the robber first so that they're aware and not surprised by your moves.
					</li>

				</ul>

			</p>

			<p>
				<img src="images/RobberyStep6.jpg" width="100%">
				<h2>6</h2>
				Focus on other people in the same predicament as you. A good way to remain calm is to assume responsibility for helping other people around you remain calm and supported. While you're busy trying to help them and be strong for them, you're less likely to give in to fearful and panicked reactions.

			</p>

			<p>
				<img src="images/RobberyStep7.jpg" width="100%">
				<h2>7</h2>
				Spend time looking for ways out of the situation. Consider whether escape is a safe possibility or not (most times it is best to wait it out). Consider whether it is worth talking to the robbers or not. Working on strategies to get through the event can help to keep you calm because you'll feel that you have at least a little control over what is happening to you.
				<ul>
					<li>
						Don't try to outsmart or clever talk the robber. Always calmly talk about your needs, such as needing to collect a child from school on time or feeling sick, etc. Help the robber to respond to you as a person, not a brave hero or a nervous wreck.
					</li>

				</ul>

			</p>

			<p>
				<img src="images/RobberyStep8.jpg" width="100%">
				<h2>8</h2>
				Go inside yourself and build a protective wall. During the minutes of the robbery, time can seem to stretch eternally. It can help by drawing on inner resources to keep yourself calm and focused on the time when it will be over. If you have a faith, draw strength from your beliefs. If not, you can still draw on your reserves of strength and belief that you will see this incident through and it will soon be all but a memory. Visualize when the time will be over and you are free again.

			</p>

			<p>
				<img src="images/RobberyStep7.jpg" width="100%">
				<h2>9</h2>
				Aim to stay calm and seek help after the robbery. Not only call the police as soon as it is safe to do so but call your therapist or seek debriefing through your workplace or through the police. You need to have the chance to talk out the experience or to air your worries to someone sympathetic and understanding. If you focused hard on remaining calm throughout the robbery, it's likely that a lot of your emotions have been delayed and the aftermath can be very confusing and anxiety-producing for you, so don't hesitate to seek help for working through your fear and trauma.
				<ul>
					<li>
						Remain calm enough after the event to try and write down all the things that you can remember about the robbers and the event. You may forget important things as time goes on and you are dealing with the aftermath of the robbery; this is a normal aspect of the brain seeking to heal a bad experience, so don't expect to be able to remember everything after time passes.
					</li>

				</ul>

